def _cmd(*cmds):
    i2c.write(OLED_ADDR, b'\x00' + bytes(cmds))

def init_oled():
    _cmd(0xAE)
    _cmd(0xD5, 0x80)
    _cmd(0xA8, 0x3F)
    _cmd(0xD3, 0x00)
    _cmd(0x40)
    _cmd(0x8D, 0x14)
    _cmd(0x20, 0x00)
    _cmd(0xA1)
    _cmd(0xC8)
    _cmd(0xDA, 0x12)
    _cmd(0x81, 0xCF)
    _cmd(0xD9, 0xF1)
    _cmd(0xDB, 0x40)
    _cmd(0x2E)
    _cmd(0xA4)
    _cmd(0xA6)
    _cmd(0xAF)

def clear_oled():
    chunk = b'\x40' + b'\x00' * 16
    for page in range(8):
        _cmd(0x22, page, page)
        _cmd(0x21, 0, 127)
        for _ in range(8):
            i2c.write(OLED_ADDR, chunk)

def _scale_nibble(nibble):
    result = 0
    for i in range(4):
        bit = (nibble >> i) & 1
        result |= (bit << (i * 2))
        result |= (bit << (i * 2 + 1))
    return result

def write_oled_large(text, start_page):
    top_data = bytearray(128)
    bot_data = bytearray(128)
    col = 0
    for char in text:
        if col + 10 > 128:
            break
        idx = ord(char) - 32
        if 0 <= idx < 95:
            for j in range(5):
                byte = FONT[idx * 5 + j]
                top = _scale_nibble(byte & 0x0F)
                bot = _scale_nibble((byte >> 4) & 0x0F)
                top_data[col + j * 2]     = top
                top_data[col + j * 2 + 1] = top
                bot_data[col + j * 2]     = bot
                bot_data[col + j * 2 + 1] = bot
        col += 12
    _cmd(0x22, start_page, start_page)
    _cmd(0x21, 0, 127)
    i2c.write(OLED_ADDR, b'\x40' + bytes(top_data))
    _cmd(0x22, start_page + 1, start_page + 1)
    _cmd(0x21, 0, 127)
    i2c.write(OLED_ADDR, b'\x40' + bytes(bot_data))

def write_oled(text, line):
    _cmd(0x22, line, line)
    _cmd(0x21, 0, 127)
    data = bytearray(128)
    col = 0
    for char in text:
        if col + 5 > 128:
            break
        idx = ord(char) - 32
        if 0 <= idx < 95:
            for j in range(5):
                data[col + j] = FONT[idx * 5 + j]
        col += 6
    i2c.write(OLED_ADDR, b'\x40' + bytes(data))
