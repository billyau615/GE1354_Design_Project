def _cmd(*cmds):
    """Send command bytes to the OLED (prefixed with 0x00 control byte)."""
    i2c.write(OLED_ADDR, b'\x00' + bytes(cmds))

def init_oled():
    """Initialize the SSD1306 OLED display."""
    _cmd(0xAE)        # display off
    _cmd(0xD5, 0x80)  # set display clock divider
    _cmd(0xA8, 0x3F)  # set multiplex ratio (64 rows)
    _cmd(0xD3, 0x00)  # set display offset
    _cmd(0x40)        # set start line 0
    _cmd(0x8D, 0x14)  # enable charge pump
    _cmd(0x20, 0x00)  # horizontal addressing mode
    _cmd(0xA1)        # segment remap (col 127 -> SEG0)
    _cmd(0xC8)        # COM scan direction (remapped)
    _cmd(0xDA, 0x12)  # set COM pins configuration
    _cmd(0x81, 0xCF)  # set contrast
    _cmd(0xD9, 0xF1)  # set pre-charge period
    _cmd(0xDB, 0x40)  # set VCOMH deselect level
    _cmd(0x2E)        # deactivate scroll
    _cmd(0xA4)        # display RAM content (not all-on)
    _cmd(0xA6)        # normal display (not inverted)
    _cmd(0xAF)        # display on

def clear_oled():
    """Clear the entire display."""
    chunk = b'\x40' + b'\x00' * 16
    for page in range(8):
        _cmd(0x22, page, page)  # set page address
        _cmd(0x21, 0, 127)      # set column address 0-127
        for _ in range(8):      # 8 * 16 = 128 bytes per page
            i2c.write(OLED_ADDR, chunk)

def write_oled(text, line):
    """Write text to the OLED at the given page line (0-7)."""
    _cmd(0x22, line, line)  # set page
    _cmd(0x21, 0, 127)      # set column 0-127
    data = bytearray(128)
    col = 0
    for char in text:
        if col + 5 > 128:
            break
        idx = ord(char) - 32
        if 0 <= idx < 95:
            for j in range(5):
                data[col + j] = FONT[idx * 5 + j]
        col += 6  # 5px char + 1px spacing
    i2c.write(OLED_ADDR, b'\x40' + bytes(data))
